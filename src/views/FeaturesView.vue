The goal of the Stichting 18 September is to keep the memory of the victims alive, especially among young people who are always on their phones. The app is a way to connect them with the history and the stories of the people who lived in their cities before the war. The project is a living tribute to those who were killed or suffered under the Nazi regime.            </p>
